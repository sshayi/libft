#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	int		i;
	char	*ptr;

	i = 0;
	ptr = (char *)s2;
	while (s2[i] != '\0')
	{
		s1[i] = ptr[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
